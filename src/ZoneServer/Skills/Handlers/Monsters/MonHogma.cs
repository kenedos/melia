		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			var forceId = ForceId.GetNew();
			Send.ZC_SKILL_MELEE_GROUND(caster, skill, farPos, forceId, null);

			var targetPos = GetRelativePosition(PosType.TargetDistance, caster, target, distance: 1.6378922, angle: 0f);

			if (!await MonsterCastTime(skill, caster, "Lethargy", CastTimeMs, target))
				return;

			await skill.Wait(TimeSpan.FromMilliseconds(500));
			targetPos = originPos.GetRelative(farPos, distance: 29.332455f, angle: 1f);
			SkillCreatePad(caster, skill, targetPos, 0.031604018f, PadName.Summon_Lethargy);
			await skill.Wait(TimeSpan.FromMilliseconds(300));
			targetPos = originPos.GetRelative(farPos, distance: 51.913921f, angle: 0f);
			SkillCreatePad(caster, skill, targetPos, 0.011262165f, PadName.Summon_Lethargy);
			await skill.Wait(TimeSpan.FromMilliseconds(100));
			targetPos = originPos.GetRelative(farPos, distance: 77.854706f, angle: 1f);
			SkillCreatePad(caster, skill, targetPos, 0.020105751f, PadName.Summon_Lethargy);
		}
	}

