		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			await skill.Wait(TimeSpan.FromMilliseconds(1200));
			var effectHitConfig = new EffectHitConfig
			{
				GroundEffect = EffectConfig.None,
				PositionDelay = 0,
				Effect = new EffectConfig("F_explosion074_green", 1f),
				Range = 20f,
				KnockdownPower = 0f,
				Delay = 1f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 60f,
				InnerRange = 0,
			};

			var position = originPos.GetRelative(farPos, distance: 60, angle: 54f);
			await EffectAndHit(skill, caster, position, effectHitConfig);
			position = originPos.GetRelative(farPos, distance: 60);
			await EffectAndHit(skill, caster, position, effectHitConfig);
			var effectHitConfig2 = new EffectHitConfig
			{
				GroundEffect = EffectConfig.None,
				PositionDelay = 0,
				Effect = new EffectConfig("F_explosion074_green", 1f),
				Range = 20f,
				KnockdownPower = 100f,
				Delay = 1f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 60f,
				InnerRange = 0,
			};

			position = originPos.GetRelative(farPos, distance: 60, angle: -54f);
			await EffectAndHit(skill, caster, position, effectHitConfig2);
			await skill.Wait(TimeSpan.FromMilliseconds(1300));
			position = originPos.GetRelative(farPos, distance: 20);
			await EffectAndHit(skill, caster, position, effectHitConfig2);
			position = originPos.GetRelative(farPos, distance: 70, angle: -45f);
			await EffectAndHit(skill, caster, position, effectHitConfig2);
			position = originPos.GetRelative(farPos, distance: 70, angle: 45f);
			await EffectAndHit(skill, caster, position, effectHitConfig2);
		}
	}

