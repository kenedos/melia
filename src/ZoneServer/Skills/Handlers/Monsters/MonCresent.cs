		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			var targetPos = originPos.GetRelative(farPos, distance: 0);
			caster.SetTargets(SkillSelectEnemiesInCircle(caster, targetPos, 70f, 5));
			var config = new EffectHitConfig
			{
				GroundEffect = new EffectConfig("F_sys_target_monster##0.3", 0.55f),
				PositionDelay = 750,
				Effect = new EffectConfig("F_rize015_1_yellow_drop", 1f),
				Range = 20f,
				KnockdownPower = 100f,
				Delay = 0f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 0f,
				InnerRange = 0f,
			};

			var position = GetRelativePosition(PosType.TargetHeight, caster, target, distance: 0);
			await EffectAndHit(skill, caster, position, config);
			position = GetRelativePosition(PosType.TargetHeight, caster, target, distance: 0);
			await EffectAndHit(skill, caster, position, config);
		}
	}
