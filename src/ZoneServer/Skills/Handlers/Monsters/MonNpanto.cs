		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			var targetPos = originPos.GetRelative(farPos);
			caster.SetTargets(SkillSelectEnemiesInCircle(caster, targetPos, 100f, 20));
			var config = new EffectHitConfig
			{
				GroundEffect = new EffectConfig("F_ground028_rize##0.3", 0.3f),
				PositionDelay = 300,
				Effect = new EffectConfig("F_rize007", 0.4f),
				Range = 20f,
				KnockdownPower = 0f,
				Delay = 100f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 60f,
				InnerRange = 0,
			};

			var position = farPos;
			await EffectAndHit(skill, caster, position, config);
			position = GetRelativePosition(PosType.TargetDistance, caster, target, rand: 50);
			await EffectAndHit(skill, caster, position, config);
			position = GetRelativePosition(PosType.TargetDistance, caster, target, rand: 50);
			await EffectAndHit(skill, caster, position, config);
		}
