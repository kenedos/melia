		{
			await skill.Wait(TimeSpan.FromMilliseconds(2500));
			var hits = new List<SkillHitInfo>();
			var position = originPos.GetRelative(farPos, distance: 50);
			await EffectAndHit(skill, caster, position, new EffectHitConfig
			{
				GroundEffect = EffectConfig.None,
				PositionDelay = 0,
				Effect = EffectConfig.None,
				Range = 50f,
				KnockdownPower = 100f,
				Delay = 50f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 60f,
				InnerRange = 0,
			}, hits);
			SkillResultTargetBuff(caster, skill, BuffId.CriticalWound_Mon, 1, hits.Sum(h => h.HitInfo.Damage) * 0.6f, 5000f, 1, 100, -1, hits);
		}
	}

