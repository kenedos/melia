	[SkillHandler(SkillId.Mon_boss_Genmagnus_pattern_Skill_1)]
	public class Mon_boss_Genmagnus_pattern_Skill_1 : ITargetSkillHandler
	{
		public void Handle(Skill skill, ICombatEntity caster, ICombatEntity target)
		{
			if (!caster.TrySpendSp(skill))
			{
				caster.ServerMessage(Localization.Get("Not enough SP."));
				return;
			}
			skill.IncreaseOverheat();
			caster.SetAttackState(true);

			var originPos = caster.Position;
			var farPos = originPos.GetNearestPositionWithinDistance(target.Position, skill.Properties[PropertyName.MaxR]);
			Send.ZC_SKILL_MELEE_GROUND(caster, skill, farPos);

			skill.Run(this.HandleSkill(caster, target, skill, originPos, farPos));
		}

		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			var hits = new List<SkillHitInfo>();
			var position = originPos.GetRelative(farPos, distance: 180);
			await EffectAndHit(skill, caster, position, new EffectHitConfig
			{
				GroundEffect = new EffectConfig("F_sys_target_monster##0.5", 4f),
				PositionDelay = 0,
				Effect = EffectConfig.None,
				Range = 80f,
				KnockdownPower = 100f,
				Delay = 0f,
				HitCount = 0,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 0f,
				InnerRange = 0f,
			}, hits);
			var targetPos = originPos.GetRelative(farPos);
			await skill.Wait(TimeSpan.FromMilliseconds(2000));
			var config = new EffectHitConfig
			{
				GroundEffect = new EffectConfig("None", 3.5f),
				PositionDelay = 0,
				Effect = EffectConfig.None,
				Range = 120f,
				KnockdownPower = 100f,
				Delay = 0f,
				HitCount = 1,
				HitDuration = 0f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 0f,
				InnerRange = 0f,
			};

			for (var i = 0; i < 4; i++)
			{
				position = originPos.GetRelative(farPos, distance: 180);
				await EffectAndHit(skill, caster, position, config, hits);

				if (i < 3)
					await skill.Wait(TimeSpan.FromMilliseconds(300));
			}
			SkillResultTargetBuff(caster, skill, BuffId.Mythic_freeze, 99, 0f, 3000f, 1, 100, -1, hits);
		}
	}

