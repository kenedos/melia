		private async Task HandleSkill(ICombatEntity caster, ICombatEntity target, Skill skill, Position originPos, Position farPos)
		{
			var splashParam = skill.GetSplashParameters(caster, originPos, farPos, length: 0, width: 0, angle: 10f);
			var splashArea = skill.GetSplashArea(SplashType.Fan, splashParam);
			var hitDelay = 0;
			var damageDelay = 300;
			await SkillAttack(caster, skill, splashArea, hitDelay, damageDelay);
			await skill.Wait(TimeSpan.FromMilliseconds(500));

			var arrowConfig = new ArrowConfig
			{
				ArrowEffect = EffectConfig.None,
				ArrowSpacing = 25f,
				ArrowSpacingTime = 0.1f,
				ArrowLifeTime = 0.6f,
				PositionDelay = 1000f,
				HitEffect = new EffectConfig("F_burstup002_dark", 0.6f),
				Range = 15f,
				KnockdownPower = 100f,
				Delay = 0f,
				HitEffectSpacing = 30f,
				HitTimeSpacing = 0.3f,
				HitCount = 1,
				HitDuration = 1000f,
			};

			for (var i = 0; i < 3; i++)
			{
				var startingPosition = originPos.GetRelative(farPos, distance: 35f);
				var endingPosition = originPos.GetRelative(farPos, distance: 250f);
				await EffectHitArrow(skill, caster, startingPosition, endingPosition, arrowConfig);
			}

			await skill.Wait(TimeSpan.FromMilliseconds(300));
			var position = originPos.GetRelative(farPos, distance: 30);
			await EffectAndHit(skill, caster, position, new EffectHitConfig
			{
				GroundEffect = EffectConfig.None,
				PositionDelay = 500,
				Effect = new EffectConfig("F_explosion098_dark_blue", 1f),
				Range = 80f,
				KnockdownPower = 0f,
				Delay = 0f,
				HitCount = 1,
				HitDuration = 1000f,
				CasterEffect = EffectConfig.None,
				CasterNodeName = "None",
				KnockType = 1,
				VerticalAngle = 0f,
				InnerRange = 0f,
			});
		}
	}

