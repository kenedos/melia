			await MissileThrow(skill, caster, position, new MissileConfig
			{
				Effect = new EffectConfig("I_archer_canon_force3#Dummy_weapon01", 1f),
				EndEffect = new EffectConfig("F_archer_CannonShot_explosion2", 0.6f),
				Range = 35f,
				FlyTime = 1f,
				DelayTime = 0f,
				Gravity = 250f,
				Speed = 1f,
				HitTime = 1000f,
				HitCount = 1,
				GroundEffect = new EffectConfig("F_sys_target_monster", 1.1f),
			});
