namespace Melia.Zone.Skills.Handlers.Common.MagicalGirl
{
	/// <summary>
	/// Handler for the MagicalGirl skill Magical Girl Mint.
	/// </summary>
	[SkillHandler(SkillId.MagicalGirl_MagicalGirlMint)]
	public class MagicalGirl_MagicalGirlMint : IGroundSkillHandler
	{
		public void Handle(Skill skill, ICombatEntity caster, Position originPos, Position farPos, ICombatEntity target)
		{
			if (!caster.TrySpendSp(skill))
			{
				caster.ServerMessage(Localization.Get("Not enough SP."));
				return;
			}
			skill.IncreaseOverheat();
			caster.SetAttackState(true);

			var targetHandle = target?.Handle ?? 0;
			Send.ZC_SKILL_READY(caster, skill, 1, originPos, farPos);
			Send.ZC_NORMAL.UpdateSkillEffect(caster, targetHandle, originPos, originPos.GetDirection(farPos), Position.Zero);
			Send.ZC_SKILL_MELEE_GROUND(caster, skill, farPos);

			skill.Run(this.HandleSkill(caster, skill, originPos, farPos));
		}

		private async Task HandleSkill(ICombatEntity caster, Skill skill, Position originPos, Position farPos)
		{
			await Task.Delay(TimeSpan.FromMilliseconds(3200));
			caster.StartBuff(BuffId.change_magicalgirl_mint_Buff, 1f, 0f, TimeSpan.FromMilliseconds(900000f), caster);
		}
	}
}
